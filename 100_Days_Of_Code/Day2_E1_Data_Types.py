""" Write a program that adds the digits in a two digits number. Example, if
    the input was 35, then the output should be 3 + 5 = 8."""

input_number = input("\nType a two digit number: ")

number1 = int(input_number[0])
number2 = int(input_number[1])

print(number1 + number2)
