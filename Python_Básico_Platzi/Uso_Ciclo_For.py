# Uso del Ciclo For:

""" Elabore un programa que permita imprimir los números del 0 al 100 y
    otro que imprima del 1 al 100
"""

# Imprimir los números del 0 al 100...
for i in range(101):
    print(i)

# Imprimir una linea divisoria entre ambos ciclos...
print("----")

# Imprimir los números del 1 al 100...
for i in range(1, 101):
    print(i)
